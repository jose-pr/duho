def test_scaffold_cmd_is_a_duho_cmd():
    """The CLI is implemented as a duho command (Cli subclass of Cmd)."""
    assert issubclass(ScaffoldCmd, duho.Cmd)


def test_cli_writes_pair_and_exits_zero(tmp_path):
    """``duho.main(ScaffoldCmd, ...)`` writes the pair and returns 0."""
    rc = duho.main(
        ScaffoldCmd,
        ["demo", "--root", str(tmp_path), "--libdir", "lib", "--force"],
    )
    assert rc == 0
    assert (tmp_path / "bin" / "demo").exists()
    assert (tmp_path / "bin" / "demo.cmd").exists()
    assert 'exec "${PYTHON:-python3}" -m demo "$@"' in _read(tmp_path / "bin" / "demo")


def test_cli_module_main_entrypoint(tmp_path):
    """The module-level ``main(argv)`` entry dispatches the command too."""
    rc = scaffold.main(["demo", "--root", str(tmp_path), "--force"])
    assert rc == 0
    assert (tmp_path / "bin" / "demo").exists()


def test_cli_force_flag_overwrites(tmp_path):
    """``--force`` on the CLI maps to ``overwrite=True``."""
    scaffold.main(["demo", "--root", str(tmp_path), "--force"])
    # Without --force a second run would refuse; with it, it succeeds (rc 0).
    rc = scaffold.main(["demo", "--root", str(tmp_path), "--force"])
    assert rc == 0


